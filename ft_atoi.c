#include"libft.h"

int	ft_atoi(const char	*str)
{
	int	i;
	int	m;
	int	cf;

	i = 0;
	m = 0;
	cf = 0;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t'
		|| str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			m++;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		cf = (cf * 10) + str[i] - '0';
		i++;
	}
	if (m % 2 == 1)
		cf = cf * (-1);
	return (cf);
}
