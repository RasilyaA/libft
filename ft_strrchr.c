#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	j;

	j = 0;
	while (s[j] != '\0')
		j++;
	while (j >= 0)
	{
		if (s[j] == (char)c)
			return ((char *)s + j);
		j--;
	}
	return (NULL);
}
